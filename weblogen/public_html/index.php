LOGEN is an offline
<a href="http://mathworld.wolfram.com/PartialEvaluation.html">partial evaluation</a>
system for 
<a href="http://en.wikipedia.org/wiki/Prolog">Prolog</a> written using the
so called "<a href="pictures/logen.png">cogen approach</a>".
Basically, the cogen is a system which:
<ol>
<li> based upon an annotated version of the program to be specialised 
 produces a specialised partial evaluator for that program. This partial
 evaluator is called a generating extension.</li>
<li> the generating extension can be used to specialise the program in 
 a very efficient manner.</li>
</ol>

Try out Logen without having to install it on your machine
 by clicking on the picture above.

